package org.nightlabs.eclipse.ui.pdfviewer.resource;

import java.util.MissingResourceException;
import java.util.ResourceBundle;

/**
 * @version $Revision: 343 $ - $Date: 2008-10-10 00:42:14 +0200 (Fr, 10 Okt 2008) $
 * @author frederik loeser - frederik at nightlabs dot de
 */
public class Messages {
	private static final String BUNDLE_NAME = "org.nightlabs.eclipse.ui.pdfviewer.resource.messages"; //$NON-NLS-1$

	private static final ResourceBundle RESOURCE_BUNDLE = ResourceBundle.getBundle(BUNDLE_NAME);

	private Messages() {
	}

	public static String getString(String key) {
		try {
			return RESOURCE_BUNDLE.getString(key);
		} catch (MissingResourceException e) {
			return '!' + key + '!';
		}
	}
}
